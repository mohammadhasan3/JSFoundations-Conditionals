// Define the numbers and the operation here
let first_int = 5;
let second_int = 10;
let operation = "/";
// Write if-statements here

if(operation === "+"){
    console.log(first_int + second_int);
}else
if(operation === "-"){
    console.log(first_int - second_int);
}else
if(operation === "*"){
    console.log(first_int * second_int);
}else{
    console.log(first_int / second_int);
}